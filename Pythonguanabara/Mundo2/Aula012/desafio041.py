idade = int(input('Qual a sua idade? '))

if idade <= 9:

    print('Sua categoria: Mirim')

elif 9 < idade <= 14:

    print('Sua categoria: Infantil')

elif 14 < idade <= 19:

    print('Sua categoria: Junior')

elif 19 < idade <= 20:

    print('Sua categoria: Senior')

elif 20 < idade:

    print('Sua categoria: Master')
