def escreva(msg):
    tam = len(msg) + 4
    print('-' * tam)
    print(f'  {msg}')
    print('-' * tam)


escreva('Pamela Agostinho Mourao')
escreva('Bruno Ribas Torres')
escreva('Alice Mourao Torres')
escreva('Alice Agostinho Ribas')
escreva('Alice Mourao Ribas')
escreva('Alice Agostinho Torres')
